  /// Suggests an emoji based on Persian task title
  /// 
  /// This method uses an advanced matching algorithm to find the most appropriate
  /// emoji for the given title. It first searches for multi-word phrases,
  /// and if none are found, searches for single words.
  /// 
  /// [title] Persian task title
  /// 
  /// Returns: Suggested emoji or null if no match is found
  static String? suggestEmoji(String title) {
    if (title.trim().isEmpty) return null;

    // Normalize the title
    final normalizedTitle = StringUtils.normalize(title);
    
    // Return null if title becomes empty after normalization
    if (normalizedTitle.isEmpty) return null;

    // Priority 1: Search for multi-word phrases (from longest to shortest)
    // Sort keys by length (descending)
    final sortedKeys = _emojiMap.keys.toList()
      ..sort((a, b) => b.length.compareTo(a.length));

    // Search in multi-word phrases
    for (final key in sortedKeys) {
      final normalizedKey = StringUtils.normalize(key);
      
      // If the key exists in the title
      if (normalizedTitle.contains(normalizedKey)) {
        return _emojiMap[key];
      }
    }

    // Priority 2: If no multi-word phrases found, check single words
    // Split title into words
    final words = normalizedTitle.split(RegExp(r'[\s\u200c]+'))
      ..removeWhere((word) => word.isEmpty);

    // Search in single words
    for (final word in words) {
      if (_emojiMap.containsKey(word)) {
        return _emojiMap[word];
      }
    }

    // If no match found
    return null;
  }

